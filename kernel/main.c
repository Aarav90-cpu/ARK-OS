void PutPixel(BootInfo *bootInfo, int x, int y, uint32_t color) {
    uint32_t *fb = (uint32_t *)bootInfo->Framebuffer.BackBuffer;
    uint32_t width = bootInfo->Framebuffer.Width;
    uint32_t height = bootInfo->Framebuffer.Height;
    uint32_t ppl = bootInfo->Framebuffer.PixelsPerScanLine;

    if (x >= 0 && x < (int)width && y >= 0 && y < (int)height) {
        fb[y * ppl + x] = color;
    }
}

void DrawChar(BootInfo *bootInfo, char c, int cx, int cy, uint32_t color) {
    const uint8_t *glyph = tiny_font[(uint8_t)c];

    for (int y = 0; y < 8; y++) {
        for (int x = 0; x < 8; x++) {
            if (glyph[y] & (1 << (7 - x))) {
                for(int dy=0; dy<4; dy++) {
                    for(int dx=0; dx<4; dx++) {
                        PutPixel(bootInfo, cx + (x*4) + dx, cy + (y*4) + dy, color);
                    }
                }
            }
        }
    }
}

void PrintString(BootInfo *bootInfo, const char *str, int x, int y, uint32_t color) {
    while (*str) {
        DrawChar(bootInfo, *str, x, y, color);
        x += 32;
        str++;
    }
}

void kernel_main(BootInfo *bootInfo) {
    __asm__ volatile("cli");
    if (!bootInfo) while(1);

    uint32_t *fbBase = bootInfo->Framebuffer.BaseAddress;
    for (int i = 0; i < 100000; i++) fbBase[i] = 0x00FF0000;

    uint32_t *backBuffer = bootInfo->Framebuffer.BackBuffer;
    uint32_t width = bootInfo->Framebuffer.Width;
    uint32_t height = bootInfo->Framebuffer.Height;
    uint32_t ppl = bootInfo->Framebuffer.PixelsPerScanLine;
    uint32_t bufferSize = bootInfo->Framebuffer.BufferSize;

    for (uint32_t y = 0; y < height; y++) {
        for (uint32_t x = 0; x < width; x++) {
            backBuffer[y * ppl + x] = 0x00000000;
        }
    }

    KLog_Init(bootInfo);

    UEFI_Init(bootInfo);
    RAM_Init(bootInfo);
    CPU_Init(bootInfo);
    Timers_Init(bootInfo);
    Motherboard_Init(bootInfo);
    Cooling_Init(bootInfo);
    Storage_Init(bootInfo);

    uint64_t *dst = (uint64_t *)fbBase;
    uint64_t *src = (uint64_t *)backBuffer;
    for (uint32_t i = 0; i < bufferSize / 8; i++) {
        dst[i] = src[i];
    }

    while (1) {
        __asm__ volatile("hlt");
    }
}

